CREATE DATABASE SALEORDER;

USE SALEORDER;
CREATE TABLE CUSTOMER(
CUSTOMER_ID INT NOT NULL PRIMARY KEY,
CUSTOMER_FIRS_TNAME VARCHAR(50) NOT NULL,
CUSTOMER_LAST_NAME VARCHAR(50) NOT NULL,
CUSTOMER_ADDRESS VARCHAR (250) NOT NULL,
CUSTOMER_SUBSCRIBE VARCHAR(250) NOT NULL,
CUSTOMER_CITY VARCHAR(50) NOT NULL,
CUSTOMER_POSTCODE CHAR(4) NULL,
CUSTOMER_CONTACT CHAR(12) NULL
 );
 
 CREATE TABLE INVENTARY(
 INVENTARY_ID TINYINT NOT NULL PRIMARY KEY,
 INVENTARY_NAME  VARCHAR(250) NOT NULL,
 INVENTARY_DESCRIPTION VARCHAR(255) NULL
 );
 
 CREATE TABLE EMPLOYEE(
 EMPLOYEE_ID TINYINT NOT NULL PRIMARY KEY,
 EMPLOYEE_FIRST_NAME VARCHAR (50) NOT NULL,
 EMPLOYEE_LAST_NAME VARCHAR (50) NOT NULL,
 EMPLOYEE_XTENSION CHAR(4) NULL
 );
 
 CREATE TABLE SALES(
 SALE_ID TINYINT NOT NULL PRIMARY KEY,
 CUSTOMER_ID INT NOT NULL references CUSTOMER (CUSTOMER_ID),
 INVENTARY_ID TINYINT NOT NULL references INVENTARY (INVENTARY_ID),
 EMPLOYEE_ID TINYINT NOT NULL references EMPLOYEE(EMPLOYEE_ID),
 SALE_DATE DATE NOT NULL,
 SALE_QUANTITY INT NOT NULL,
 SALE_UNIT_PRICE decimal(10,2) NOT NULL
 );
 
